package Apartado3;

public class Insignia {
    private String nom;
    private int nivell;

    public Insignia(String nom, int nivell){
        this.nom = nom;
        this.nivell = nivell;
    }
    public String getNom() {
        return this.nom;
    }
    public int getNivell() {
        return this.nivell;
    }
}
